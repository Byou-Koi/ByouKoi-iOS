import Foundation
import UIKit

extension UIColor {
    class func mainTextColor() -> UIColor {
        return UIColor(red: 117/255, green: 117/255, blue: 117/255, alpha: 1.0)
    }
    
    class func subTextColor() -> UIColor {
        return UIColor(red: 170/255, green: 170/255, blue: 170/255, alpha: 1.0)
    }
}
